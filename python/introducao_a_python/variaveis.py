# Variáveis

a = 10
b = 5

# Calculos aritméticos

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b

# Imprimindo variáveis

print('Soma: %s' %(soma))
print('Subtração: %s' %(subtracao))
print('Multiplicação: %s' %(multiplicacao))
print('Divisão: %.0f' %(divisao))
print('Resto: %s' %(resto))